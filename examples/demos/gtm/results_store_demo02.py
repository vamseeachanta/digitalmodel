#!/usr/bin/env python3
# ABOUTME: demo_02 Results Store — SQLite fast-read cache + per-run CSV/manifest text source.
# ABOUTME: write_run() persists one run; rebuild_db() regenerates the cache per-cell-identically from text.
"""demo_02 Results Store (sqlite3 stdlib only), adapted from the demo_01 Store.

This module is *additive*: it never changes demo_02's existing JSON or HTML
outputs. Alongside them it adds a queryable artifact set under ``<base_dir>``::

    parametric/demo_02/index.csv                 (one row per run — DERIVED)
    parametric/demo_02/results.db                (SQLite cache — DERIVED, gitignored)
    parametric/demo_02/<run_id>/cases.csv        (enriched per-case table — SOURCE)
    parametric/demo_02/<run_id>/manifest.json    (resolved config snapshot — SOURCE)

The per-run ``cases.csv`` + ``manifest.json`` are the text source of truth;
``index.csv`` and ``results.db`` are derived and regenerated by
:func:`rebuild_db`. The rebuild is verified (tests) to be per-cell and
per-``typeof`` identical to the direct-write path.

demo_02 specifics vs demo_01:

  * BD-1  ``is_safe`` is stored as a stable TEXT token ("True"/"False"/NULL),
          never as an INTEGER, on BOTH the direct-write and rebuild paths.
  * BD-2  the nested ``checks`` dict is flattened into four REAL columns
          ``check_pressure_containment`` / ``check_collapse`` /
          ``check_propagation_buckling`` / ``check_combined_loading``.
  * BD-3  empty-string / None map to SQL NULL *before* any numeric cast, so a
          null round-trips as NULL (never ``0`` or the string ``"nan"``).
  * BD-4  the composite primary key is
          ``(run_id, pipe_size, code, internal_pressure_mpa)``.
"""
from __future__ import annotations

import csv
import json
import re
import sqlite3
import subprocess
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Optional

try:
    import pandas as pd
except ImportError as exc:  # pragma: no cover — real dep required.
    raise RuntimeError("results_store_demo02 requires pandas") from exc


DEMO_ID = "demo_02"

# A run_id becomes a directory name under the Store, so it must be a single safe
# path segment (D1 defense-in-depth: the caller may also validate).
_RUN_ID_RE = re.compile(r"^[A-Za-z0-9._-]+$")


def _validate_run_id(run_id: str) -> str:
    """Reject a run_id that is not a single safe path segment (D1, path-traversal guard)."""
    if not isinstance(run_id, str) or run_id in ("", ".", "..") or not _RUN_ID_RE.match(run_id):
        raise ValueError(
            f"invalid run_id {run_id!r}: must match [A-Za-z0-9._-]+ and may not be "
            "'', '.', or '..' (it becomes a directory name in the Results Store)."
        )
    return run_id


# The four nested-check keys, in canonical order; flattened into ``check_<key>`` (BD-2).
_CHECK_KEYS = (
    "pressure_containment",
    "collapse",
    "propagation_buckling",
    "combined_loading",
)

# ---------------------------------------------------------------------------
# Explicit typed schema for the `cases` table (NB-3: every column an explicit type).
# Order is canonical for CSV + SQLite on BOTH paths. `run_id` is the partition key.
# ---------------------------------------------------------------------------
_CASE_COLUMNS: List[tuple[str, str]] = [
    ("run_id", "TEXT"),
    # --- inputs / axes ---
    ("pipe_size", "TEXT"),            # has the literal '"' character
    ("od_mm", "REAL"),
    ("od_m", "REAL"),
    ("wt_mm", "REAL"),
    ("wt_m", "REAL"),
    ("grade", "TEXT"),
    ("code", "TEXT"),
    ("internal_pressure_mpa", "INTEGER"),
    ("external_pressure_mpa", "REAL"),
    ("water_depth_m", "REAL"),
    # --- outputs ---
    ("is_safe", "TEXT"),              # BD-1: TEXT token "True"/"False"/NULL, never INTEGER
    ("governing_check", "TEXT"),
    ("max_utilisation", "REAL"),
    # --- flattened checks{} (BD-2) ---
    ("check_pressure_containment", "REAL"),
    ("check_collapse", "REAL"),
    ("check_propagation_buckling", "REAL"),
    ("check_combined_loading", "REAL"),
]

_CASE_COL_NAMES = [name for name, _ in _CASE_COLUMNS]
_CASE_COL_TYPES = {name: typ for name, typ in _CASE_COLUMNS}

_REAL_COLS = {name for name, typ in _CASE_COLUMNS if typ == "REAL"}
_INT_COLS = {name for name, typ in _CASE_COLUMNS if typ == "INTEGER"}
_TEXT_COLS = {name for name, typ in _CASE_COLUMNS if typ == "TEXT"}

# BD-4: composite primary key.
_PK_COLUMNS = ("run_id", "pipe_size", "code", "internal_pressure_mpa")

_RUNS_COLUMNS: List[tuple[str, str]] = [
    ("run_id", "TEXT PRIMARY KEY"),
    ("utc", "TEXT"),
    ("resolved_yaml", "TEXT"),
    ("git_sha", "TEXT"),
    ("total_cases", "INTEGER"),
    ("n_safe", "INTEGER"),
    ("n_unsafe", "INTEGER"),
]

_INDEX_COLUMNS = [
    "run_id", "utc", "total_cases", "n_safe", "n_unsafe", "input_ref", "git_sha",
]


# ---------------------------------------------------------------------------
# Serialization helpers
# ---------------------------------------------------------------------------

def _bool_token(value: Any) -> Optional[str]:
    """Map a Python bool/None to a stable SQLite TEXT token (BD-1).

    ``None`` -> ``None`` (SQL NULL); ``True`` -> ``"True"``; ``False`` ->
    ``"False"``. Never an INTEGER. On the CSV round-trip ``value`` arrives as
    the strings "True"/"False"/"" — those pass straight through.
    """
    if value is None:
        return None
    if isinstance(value, str):
        s = value.strip()
        if s == "":
            return None
        if s in ("True", "False"):
            return s
        raise ValueError(f"unexpected is_safe token {value!r}; expected True/False/empty")
    return "True" if value else "False"


def _git_sha() -> str:
    """Current git SHA, or "unknown" on any failure (detached/missing/non-repo)."""
    try:
        out = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            capture_output=True, text=True, check=True,
            cwd=Path(__file__).resolve().parent,
        )
        return out.stdout.strip() or "unknown"
    except Exception:
        return "unknown"


def _utc_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def _serialize_resolved_config(config: Any) -> Dict[str, Any]:
    """Serialize a ResolvedDemo02Config to a JSON-safe dict snapshot.

    Works structurally (no import of the dataclass type) so the module stays
    decoupled. DesignCode enums become their display strings; SafetyClass
    becomes its name.
    """
    if config is None:
        return {}
    design_codes = getattr(config, "design_codes", []) or []
    safety_class = getattr(config, "safety_class", None)
    return {
        "demo_id": getattr(config, "demo_id", DEMO_ID),
        "code_ref": getattr(config, "code_ref", ""),
        "sizes": list(getattr(config, "sizes", []) or []),
        "codes": list(getattr(config, "codes", []) or []),  # SPACED display strings
        "design_codes": [getattr(c, "value", str(c)) for c in design_codes],
        "internal_pressures_mpa": list(getattr(config, "internal_pressures_mpa", []) or []),
        "water_depth_m": getattr(config, "water_depth_m", None),
        "grade": getattr(config, "grade", None),
        "smys_pa": getattr(config, "smys_pa", None),
        "smts_pa": getattr(config, "smts_pa", None),
        "corrosion_allowance_m": getattr(config, "corrosion_allowance_m", None),
        "safety_class": (
            getattr(safety_class, "name", None) if safety_class is not None else None
        ),
        "find_min_bounds_m": list(getattr(config, "find_min_bounds_m", []) or []),
        "find_min_tol_m": getattr(config, "find_min_tol_m", None),
        "source_path": str(getattr(config, "source_path", "")),
    }


def _input_ref(config: Any) -> str:
    return str(getattr(config, "source_path", "") or "")


def _count_safe(records: List[Dict[str, Any]]) -> tuple[int, int]:
    """NB-1: n_safe = count(is_safe True); n_unsafe = count(is_safe False); a
    null (None) is neither.

    Counting goes through the canonical TEXT token (``_bool_token``) rather than a
    Python ``is True``/``is False`` identity check, so numpy.bool_ values (which
    the analyzer emits for ~1/3 of cases) are classified correctly instead of
    silently dropping into the "neither" bucket.
    """
    tokens = [_bool_token(r.get("is_safe")) for r in records]
    n_safe = sum(1 for t in tokens if t == "True")
    n_unsafe = sum(1 for t in tokens if t == "False")
    return n_safe, n_unsafe


# ---------------------------------------------------------------------------
# Path helpers
# ---------------------------------------------------------------------------

def _store_root(base_dir: Path) -> Path:
    return Path(base_dir) / "parametric" / DEMO_ID


def _db_path(base_dir: Path) -> Path:
    return _store_root(base_dir) / "results.db"


def _index_path(base_dir: Path) -> Path:
    return _store_root(base_dir) / "index.csv"


# ---------------------------------------------------------------------------
# Row coercion
# ---------------------------------------------------------------------------

def _flatten_record(run_id: str, rec: Dict[str, Any]) -> Dict[str, Any]:
    """Map a 14-key demo_02 record into the flat column dict (BD-2).

    The nested ``checks`` dict is flattened into the four ``check_*`` columns and
    the nested key dropped. ``is_safe`` becomes a TEXT token (BD-1). The built
    row's key set is asserted to equal the declared column set before use, so a
    schema drift fails loudly.
    """
    checks = rec.get("checks") or {}
    row: Dict[str, Any] = {
        "run_id": run_id,
        "pipe_size": rec.get("pipe_size"),
        "od_mm": rec.get("od_mm"),
        "od_m": rec.get("od_m"),
        "wt_mm": rec.get("wt_mm"),
        "wt_m": rec.get("wt_m"),
        "grade": rec.get("grade"),
        "code": rec.get("code"),
        "internal_pressure_mpa": rec.get("internal_pressure_mpa"),
        "external_pressure_mpa": rec.get("external_pressure_mpa"),
        "water_depth_m": rec.get("water_depth_m"),
        "is_safe": _bool_token(rec.get("is_safe")),
        "governing_check": rec.get("governing_check"),
        "max_utilisation": rec.get("max_utilisation"),
    }
    for k in _CHECK_KEYS:
        row[f"check_{k}"] = checks.get(k)

    declared = set(_CASE_COL_NAMES)
    assert set(row.keys()) == declared, (
        "demo_02 case row keys do not match declared columns: "
        f"{sorted(set(row.keys()) ^ declared)}"
    )
    return row


def _coerce_cell(value: Any, sqlite_type: str, col: str) -> Any:
    """Coerce a value (native or CSV-read string) to the typed cell value.

    BD-3 / rebuild determinism: empty string and ``None`` both map to SQL NULL
    *before* any numeric cast, for ANY nullable column — so a null round-trips
    as NULL, never ``0`` or the string ``"nan"``.
    """
    if value is None:
        return None
    if isinstance(value, str) and value == "":
        return None
    if col == "is_safe":
        return _bool_token(value)  # already a token "True"/"False"; pass through
    if sqlite_type == "REAL":
        return float(value)
    if sqlite_type == "INTEGER":
        return int(value)
    return str(value)


def _typed_case_row(run_id: str, raw: Dict[str, Any], *, flatten: bool) -> tuple:
    """Build a typed value tuple in canonical column order.

    When *flatten* is True, *raw* is a 14-key demo_02 record (nested checks);
    otherwise *raw* is a flat CSV row dict (strings). Both produce per-cell
    identical typed tuples.
    """
    flat = _flatten_record(run_id, raw) if flatten else raw
    values: List[Any] = []
    for name in _CASE_COL_NAMES:
        if name == "run_id":
            values.append(run_id)
            continue
        values.append(_coerce_cell(flat.get(name), _CASE_COL_TYPES[name], name))
    return tuple(values)


# ---------------------------------------------------------------------------
# SQLite schema + transactional per-run write (copied from demo_01 machinery)
# ---------------------------------------------------------------------------

def _create_schema(conn: sqlite3.Connection) -> None:
    runs_cols = ", ".join(f"{n} {t}" for n, t in _RUNS_COLUMNS)
    case_cols = ", ".join(f"{n} {t}" for n, t in _CASE_COLUMNS)
    pk = ", ".join(_PK_COLUMNS)
    conn.execute(f"CREATE TABLE IF NOT EXISTS runs ({runs_cols})")
    conn.execute(f"CREATE TABLE IF NOT EXISTS cases ({case_cols}, PRIMARY KEY ({pk}))")


def _upsert_run_into_db(
    conn: sqlite3.Connection,
    run_id: str,
    run_row: Dict[str, Any],
    case_rows: List[tuple],
) -> None:
    """Transactionally replace a run: delete-then-insert (never duplicate/orphan)."""
    placeholders = ", ".join("?" for _ in _CASE_COL_NAMES)
    run_cols = [n for n, _ in _RUNS_COLUMNS]
    run_placeholders = ", ".join("?" for _ in run_cols)
    conn.execute("BEGIN")
    try:
        conn.execute("DELETE FROM cases WHERE run_id=?", (run_id,))
        conn.execute("DELETE FROM runs WHERE run_id=?", (run_id,))
        conn.execute(
            f"INSERT INTO runs ({', '.join(run_cols)}) VALUES ({run_placeholders})",
            tuple(run_row[c] for c in run_cols),
        )
        conn.executemany(
            f"INSERT INTO cases ({', '.join(_CASE_COL_NAMES)}) VALUES ({placeholders})",
            case_rows,
        )
        conn.execute("COMMIT")
    except Exception:
        conn.execute("ROLLBACK")
        raise


# ---------------------------------------------------------------------------
# CSV writer + index upsert / regenerate
# ---------------------------------------------------------------------------

def _write_cases_csv(path: Path, run_id: str, records: List[Dict[str, Any]]) -> None:
    """Write the enriched per-case table to CSV (no run_id column); None -> ""."""
    csv_cols = [c for c in _CASE_COL_NAMES if c != "run_id"]
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.writer(fh)
        writer.writerow(csv_cols)
        for rec in records:
            flat = _flatten_record(run_id, rec)
            writer.writerow(["" if flat[c] is None else flat[c] for c in csv_cols])


def _write_index(path: Path, rows: List[Dict[str, Any]]) -> None:
    """Write index.csv from `rows`, each normalized to `_INDEX_COLUMNS` (missing -> "")."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as fh:
        writer = csv.DictWriter(fh, fieldnames=_INDEX_COLUMNS)
        writer.writeheader()
        for r in rows:
            writer.writerow({k: ("" if r.get(k) is None else str(r.get(k))) for k in _INDEX_COLUMNS})


def _index_row_from_manifest(manifest: Dict[str, Any]) -> Dict[str, Any]:
    """Derive one index.csv row from a per-run manifest.json (the rebuild source)."""
    return {
        "run_id": manifest["run_id"],
        "utc": manifest["utc"],
        "total_cases": int(manifest["total_cases"]),
        "n_safe": int(manifest["n_safe"]),
        "n_unsafe": int(manifest["n_unsafe"]),
        "input_ref": manifest.get("input_ref", ""),
        "git_sha": manifest.get("git_sha", ""),
    }


def _upsert_index(base_dir: Path, row: Dict[str, Any]) -> None:
    """Read existing index.csv, replace the row with same run_id, rewrite (not blind append)."""
    path = _index_path(base_dir)
    rows: List[Dict[str, str]] = []
    if path.exists():
        with path.open("r", newline="", encoding="utf-8") as fh:
            rows = [
                {k: r.get(k, "") for k in _INDEX_COLUMNS}
                for r in csv.DictReader(fh)
            ]
    rows = [r for r in rows if r.get("run_id") != row["run_id"]]
    rows.append({k: row.get(k, "") for k in _INDEX_COLUMNS})
    _write_index(path, rows)


# ---------------------------------------------------------------------------
# Public API
# ---------------------------------------------------------------------------

def write_run(
    run_id: str,
    config: Any,
    results: List[Dict[str, Any]],
    base_dir: Path,
) -> Path:
    """Persist one run: per-run cases.csv + manifest.json, index.csv upsert, SQLite upsert.

    Artifacts are written sequentially:
        1. ``<run_id>/cases.csv``     (text source of truth)
        2. ``<run_id>/manifest.json`` (text source of truth)
        3. ``index.csv``             (derived view — upsert by run_id)
        4. ``results.db``            (derived SQLite cache, transactional)

    ``rebuild_db`` is the recovery path: it regenerates both derived artifacts
    from the per-run cases.csv + manifest.json. Returns the per-run directory.
    """
    _validate_run_id(run_id)
    base_dir = Path(base_dir)
    run_dir = _store_root(base_dir) / run_id
    run_dir.mkdir(parents=True, exist_ok=True)

    records = list(results)
    total_cases = len(records)
    n_safe, n_unsafe = _count_safe(records)
    utc = _utc_now()
    git_sha = _git_sha()
    resolved_snapshot = _serialize_resolved_config(config)
    input_ref = _input_ref(config)

    # 1. cases.csv
    _write_cases_csv(run_dir / "cases.csv", run_id, records)

    # 2. manifest.json
    manifest = {
        "run_id": run_id,
        "demo_id": DEMO_ID,
        "utc": utc,
        "git_sha": git_sha,
        "input_ref": input_ref,
        "total_cases": total_cases,
        "n_safe": n_safe,
        "n_unsafe": n_unsafe,
        "resolved_config": resolved_snapshot,
    }
    with (run_dir / "manifest.json").open("w", encoding="utf-8") as fh:
        json.dump(manifest, fh, indent=2, sort_keys=True)

    # 3. index.csv upsert
    _upsert_index(base_dir, {
        "run_id": run_id,
        "utc": utc,
        "total_cases": total_cases,
        "n_safe": n_safe,
        "n_unsafe": n_unsafe,
        "input_ref": input_ref,
        "git_sha": git_sha,
    })

    # 4. SQLite upsert (delete-then-insert, transactional)
    case_rows = [_typed_case_row(run_id, rec, flatten=True) for rec in records]
    run_row = {
        "run_id": run_id,
        "utc": utc,
        "resolved_yaml": json.dumps(resolved_snapshot, sort_keys=True),
        "git_sha": git_sha,
        "total_cases": total_cases,
        "n_safe": n_safe,
        "n_unsafe": n_unsafe,
    }
    conn = sqlite3.connect(_db_path(base_dir))
    try:
        _create_schema(conn)
        _upsert_run_into_db(conn, run_id, run_row, case_rows)
    finally:
        conn.close()

    return run_dir


def rebuild_db(base_dir: Path) -> Path:
    """Drop + recreate the SQLite cache AND index.csv from per-run cases.csv + manifest.json.

    Reads CSVs with ``dtype=str, keep_default_na=False`` so empty cells stay ""
    (then map to NULL, BD-3) and the is_safe token stays text. The result is
    per-cell + per-``typeof`` identical to the direct-write db.
    """
    base_dir = Path(base_dir)
    db_path = _db_path(base_dir)
    if db_path.exists():
        db_path.unlink()
    root = _store_root(base_dir)
    index_rows: List[Dict[str, Any]] = []

    conn = sqlite3.connect(db_path)
    try:
        _create_schema(conn)
        if not root.exists():
            _write_index(_index_path(base_dir), index_rows)
            return db_path
        for run_dir in sorted(p for p in root.iterdir() if p.is_dir()):
            cases_csv = run_dir / "cases.csv"
            manifest_path = run_dir / "manifest.json"
            if not (cases_csv.exists() and manifest_path.exists()):
                continue
            run_id = run_dir.name

            # keep_default_na=False stops pandas turning blanks into NaN; dtype=str
            # keeps the is_safe token + numeric strings as-is for explicit coercion.
            df = pd.read_csv(cases_csv, dtype=str, keep_default_na=False)

            with manifest_path.open("r", encoding="utf-8") as fh:
                manifest = json.load(fh)
            run_row = {
                "run_id": run_id,
                "utc": manifest["utc"],
                "resolved_yaml": json.dumps(manifest["resolved_config"], sort_keys=True),
                "git_sha": manifest["git_sha"],
                "total_cases": int(manifest["total_cases"]),
                "n_safe": int(manifest["n_safe"]),
                "n_unsafe": int(manifest["n_unsafe"]),
            }
            case_rows = [
                _typed_case_row(run_id, row.to_dict(), flatten=False)
                for _, row in df.iterrows()
            ]
            _upsert_run_into_db(conn, run_id, run_row, case_rows)
            index_rows.append(_index_row_from_manifest(manifest))
    finally:
        conn.close()

    _write_index(_index_path(base_dir), index_rows)
    return db_path


def _main(argv: Optional[List[str]] = None) -> int:
    import argparse

    parser = argparse.ArgumentParser(description="demo_02 results store")
    sub = parser.add_subparsers(dest="cmd", required=True)
    p_rebuild = sub.add_parser("rebuild")
    p_rebuild.add_argument("--base-dir", required=True)
    args = parser.parse_args(argv)
    if args.cmd == "rebuild":
        rebuild_db(args.base_dir)
        return 0
    return 1


if __name__ == "__main__":
    raise SystemExit(_main())
