#!/usr/bin/env python3
# ABOUTME: demo_05 Results Store — SQLite fast-read cache + per-run CSV/manifest text source.
# ABOUTME: write_run() persists one run; rebuild_db() regenerates the cache per-cell-identically from text.
"""demo_05 Results Store (sqlite3 stdlib only), adapted from the demo_04 Store.

This module is *additive*: it never changes demo_05's existing JSON or HTML
outputs. Alongside them it adds a queryable artifact set under ``<base_dir>``::

    parametric/demo_05/index.csv                 (one row per run — DERIVED)
    parametric/demo_05/results.db                (SQLite cache — DERIVED, gitignored)
    parametric/demo_05/<run_id>/cases.csv        (enriched per-case table — SOURCE)
    parametric/demo_05/<run_id>/manifest.json    (resolved config snapshot — SOURCE)

The per-run ``cases.csv`` + ``manifest.json`` are the text source of truth;
``index.csv`` and ``results.db`` are derived and regenerated by
:func:`rebuild_db`. The rebuild is verified (tests) to be per-cell and
per-``typeof`` identical to the direct-write path.

demo_05 specifics vs demo_04:

  * BD-1  every phase block is built via
          ``record.get("phases", {}).get(<phase name>, {}).get(<key>)`` so the
          ERROR path's ``phases={}`` (0 of 300 on the baseline, but the schema must
          survive it) yields NULL with ``typeof='null'`` for every phase column,
          on BOTH the direct-write and rebuild paths.
  * BD-2  the five phase dicts are flattened into prefixed columns
          (``lift_off_``/``in_air_bending_``/``splash_zone_``/``lowering_``/
          ``tie_in_``) so repeated keys (``utilisation``/``status``/``daf``/
          ``crane_swl_te``) never collide. ``phase`` and ``status`` are TEXT
          tokens; every numeric phase output is REAL.
  * BD-3  ``water_depth_m`` is INTEGER, ``hs_m`` is REAL. Empty-string / None
          map to SQL NULL *before* any numeric cast, so a null round-trips as NULL.
  * SENT  demo_05's baseline is all-feasible (no sentinel), but the ERROR path
          sets ``phases={}`` and ``max_utilisation=None``. The store round-trips
          ``None`` as SQL NULL identically on direct write AND on rebuild-from-
          cases.csv (never ``0`` or ``"nan"``).
  * PK    the composite primary key is
          ``(run_id, vessel_id, jumper_id, water_depth_m, hs_m)`` (run_id first).
"""
