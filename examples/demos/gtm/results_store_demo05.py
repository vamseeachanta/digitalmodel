def _flatten_record(run_id: str, rec: Dict[str, Any]) -> Dict[str, Any]:
    """Map a demo_05 record into the flat 56-column dict (BD-1 / BD-2).

    Phase blocks are read defensively via
    ``rec.get("phases", {}).get(<phase>, {}).get(<key>)`` so the ERROR path's
    ``phases={}`` produces None (-> NULL) for every phase column. The built row's
    key set is asserted to equal the declared column set, so a schema drift fails
    loudly.
    """
    phases = rec.get("phases") or {}
    row: Dict[str, Any] = {
        "run_id": run_id,
        "vessel_id": rec.get("vessel_id"),
        "vessel": rec.get("vessel"),
        "jumper_id": rec.get("jumper_id"),
        "jumper_name": rec.get("jumper_name"),
        "length_m": rec.get("length_m"),
        "water_depth_m": rec.get("water_depth_m"),
        "hs_m": rec.get("hs_m"),
        "tp_s": rec.get("tp_s"),
        "overall_status": rec.get("overall_status"),
        "governing_phase": rec.get("governing_phase"),
        "max_utilisation": rec.get("max_utilisation"),
    }
    for prefix, suffixes in _PHASE_SPECS:
        phase_block = phases.get(prefix, {}) or {}
        for suffix, _typ in suffixes:
            row[_phase_col(prefix, suffix)] = phase_block.get(suffix)

    declared = set(_CASE_COL_NAMES)
    assert set(row.keys()) == declared, (
        "demo_05 case row keys do not match declared columns: "
        f"{sorted(set(row.keys()) ^ declared)}"
    )
    return row


def _coerce_cell(value: Any, sqlite_type: str, col: str) -> Any:
    """Coerce a value (native or CSV-read string) to the typed cell value.

    BD-3 / SENT / rebuild determinism: empty string and ``None`` both map to SQL
    NULL *before* any numeric cast, for ANY nullable column — so a null round-trips
    as NULL, never ``0`` or the string ``"nan"``.

    demo_05 has no BOOLEAN-origin columns (``_BOOL_COLUMNS`` is empty); the branch is
    retained so the coercion idiom matches demo_04 exactly.
    """
    if value is None:
        return None
    if isinstance(value, str) and value == "":
        return None
    if col in _BOOL_COLUMNS:  # pragma: no cover — demo_05 has no bool columns.
        if isinstance(value, bool):
            return int(value)
        if isinstance(value, (int, float)):
            return int(value)
        s = str(value).strip()
        if s in ("True", "true", "1", "1.0"):
            return 1
        if s in ("False", "false", "0", "0.0"):
            return 0
        return int(float(s))
    if sqlite_type == "REAL":
        return float(value)
    if sqlite_type == "INTEGER":
        return int(value)
    return str(value)


def _csv_cell(value: Any, col: str) -> str:
    """Render a flattened cell for CSV. None -> "". demo_05 has no BOOLEAN-origin
    columns, so the rendering is just str(value) (empty string for None)."""
    if value is None:
        return ""
    if col in _BOOL_COLUMNS:  # pragma: no cover — demo_05 has no bool columns.
        if isinstance(value, bool):
            return "1" if value else "0"
        return "1" if int(value) else "0"
    return str(value)


def _typed_case_row(run_id: str, raw: Dict[str, Any], *, flatten: bool) -> tuple:
    """Build a typed value tuple in canonical column order.

    When *flatten* is True, *raw* is a demo_05 record (nested phases); otherwise
    *raw* is a flat CSV row dict (strings). Both produce per-cell identical typed
    tuples.
    """
    flat = _flatten_record(run_id, raw) if flatten else raw
    values: List[Any] = []
    for name in _CASE_COL_NAMES:
        if name == "run_id":
            values.append(run_id)
            continue
        values.append(_coerce_cell(flat.get(name), _CASE_COL_TYPES[name], name))
    return tuple(values)


