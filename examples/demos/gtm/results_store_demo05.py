DEMO_ID = "demo_05"

# A run_id becomes a directory name under the Store, so it must be a single safe
# path segment (D1 defense-in-depth: the caller may also validate).
_RUN_ID_RE = re.compile(r"^[A-Za-z0-9._-]+$")


def _validate_run_id(run_id: str) -> str:
    """Reject a run_id that is not a single safe path segment (D1, path-traversal guard)."""
    if not isinstance(run_id, str) or run_id in ("", ".", "..") or not _RUN_ID_RE.match(run_id):
        raise ValueError(
            f"invalid run_id {run_id!r}: must match [A-Za-z0-9._-]+ and may not be "
            "'', '.', or '..' (it becomes a directory name in the Results Store)."
        )
    return run_id


# ---------------------------------------------------------------------------
# Phase schema (BD-2): each phase contributes prefixed columns. The prefix is the
# phase key into record["phases"]; the suffix is the key inside the phase dict.
# (prefix, [(suffix, sqlite_type), ...]) — order is canonical.
#
# Type notes:
#   * phase / status are TEXT tokens (never INTEGER).
#   * every other phase output is a REAL (utilisations, forces, spans, DAFs).
#     crane_swl_te / wire_mbl_te / tolerance_mm come straight from the catalog as
#     numbers, so they are REAL too (round-trips a 600 te SWL as REAL 600.0).
#   * the NEW span-model fields (lift_span_m, w_eff_n_per_m, tiein_span_m,
#     resultant_load_n_per_m, ...) are REAL.
# ---------------------------------------------------------------------------
