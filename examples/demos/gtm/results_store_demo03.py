def _phase_col(slug: str, suffix: str) -> str:
    """Hyphen-preserving prefixed column name, e.g. ``lift-off_hook_load_kn`` (BD-2)."""
    return f"{slug}_{suffix}"


def _q(identifier: str) -> str:
    """Quote a SQL identifier so hyphen-bearing column names (``lift-off_*``,
    ``in-air_*``) are legal in DDL/DML (SQLite double-quote identifier escaping)."""
    return '"' + identifier.replace('"', '""') + '"'


