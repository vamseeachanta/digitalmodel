def _git_sha() -> str:
    """Current git SHA, or "unknown" on any failure (detached/missing/non-repo)."""
    try:
        out = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            capture_output=True, text=True, check=True,
            cwd=Path(__file__).resolve().parent,
        )
        return out.stdout.strip() or "unknown"
    except Exception:
        return "unknown"


def _utc_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def _json_safe(value: Any) -> Any:
    """Recursively coerce a config value to a JSON-safe primitive (N4).

    Paths -> str; enums -> .value/.name/str; lists/tuples -> list; dicts -> dict;
    primitives pass through; anything else -> str.
    """
    if value is None or isinstance(value, (bool, int, float, str)):
        return value
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, (list, tuple)):
        return [_json_safe(v) for v in value]
    if isinstance(value, dict):
        return {str(k): _json_safe(v) for k, v in value.items()}
    if hasattr(value, "value"):
        return _json_safe(getattr(value, "value"))
    if hasattr(value, "name"):
        return _json_safe(getattr(value, "name"))
    return str(value)


def _serialize_resolved_config(config: Any) -> Dict[str, Any]:
    """Serialize a ResolvedDemo03Config to a JSON-safe dict snapshot (N4).

    Works structurally (no import of the dataclass type) so the module stays
    decoupled. Every field is read via ``getattr`` and made JSON-safe.
    """
    if config is None:
        return {}
    fields = [
        "demo_id", "code_ref",
        "vessels", "depths", "mudmats", "hs",
        "daf_liftoff", "daf_splash", "wire_mbl_sf", "tilt_limit_deg",
        "operating_radius_m", "reference_hs", "tp_coefficient",
        "go_marginal_threshold", "nogo_utilisation",
        "seawater_density_kg_m3", "gravity_m_s2", "steel_density_kg_m3",
        "undrained_shear_strength_su_kpa", "bearing_capacity_factor_nc",
        "apply_shape_factor", "apply_depth_factor", "factor_of_safety",
        "vessels_path", "mudmats_path", "results_root", "output_root",
        "source_path",
    ]
    snapshot: Dict[str, Any] = {}
    for name in fields:
        snapshot[name] = _json_safe(getattr(config, name, None))
    return snapshot


def _input_ref(config: Any) -> str:
    return str(getattr(config, "source_path", "") or "")


def _count_status(records: List[Dict[str, Any]]) -> Dict[str, int]:
    """n_go/n_marginal/n_nogo/n_error from overall_status (GO/MARGINAL/NO_GO/ERROR)."""
    counts = {"n_go": 0, "n_marginal": 0, "n_nogo": 0, "n_error": 0}
    for r in records:
        status = r.get("overall_status")
        if status == "GO":
            counts["n_go"] += 1
        elif status == "MARGINAL":
            counts["n_marginal"] += 1
        elif status == "NO_GO":
            counts["n_nogo"] += 1
        elif status == "ERROR":
            counts["n_error"] += 1
    return counts


