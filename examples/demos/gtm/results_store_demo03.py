# ABOUTME: demo_03 Results Store — SQLite fast-read cache + per-run CSV/manifest text source.
# ABOUTME: write_run() persists one run; rebuild_db() regenerates the cache per-cell-identically from text.
"""demo_03 Results Store (sqlite3 stdlib only), adapted from the demo_02 Store.

This module is *additive*: it never changes demo_03's existing JSON or HTML
outputs. Alongside them it adds a queryable artifact set under ``<base_dir>``::

    parametric/demo_03/index.csv                 (one row per run — DERIVED)
    parametric/demo_03/results.db                (SQLite cache — DERIVED, gitignored)
    parametric/demo_03/<run_id>/cases.csv        (enriched per-case table — SOURCE)
    parametric/demo_03/<run_id>/manifest.json    (resolved config snapshot — SOURCE)

The per-run ``cases.csv`` + ``manifest.json`` are the text source of truth;
``index.csv`` and ``results.db`` are derived and regenerated by
:func:`rebuild_db`. The rebuild is verified (tests) to be per-cell and
per-``typeof`` identical to the direct-write path.

demo_03 specifics vs demo_02:

  * BD-1  every phase block is built via
          ``record.get("phases", {}).get(<display phase>, {}).get(<key>)`` so the
          ERROR path's ``phases={}`` (0 of 180, but the schema must survive it)
          yields NULL with ``typeof='null'`` for every phase + utilisation column,
          on BOTH the direct-write and rebuild paths.
  * BD-2  the five phase dicts are flattened into prefixed columns using the
          hyphen-preserving slug (``lift-off_``/``in-air_``/``splash_zone_``/
          ``lowering_``/``landing_``) so repeated keys (``phase``/``status``/
          ``utilisation``/``crane_swl_kn``/``w_sub_kn``) never collide. ``status``,
          ``phase`` and ``governing_phase`` are TEXT tokens, never INTEGER.
  * BD-3  ``water_depth_m`` is INTEGER, ``hs_m`` REAL, ``mass_te`` REAL, and the
          lowering ``depth_m`` is INTEGER. Empty-string / None map to SQL NULL
          *before* any numeric cast, so a null round-trips as NULL.
  * PK    the composite primary key is
          ``(run_id, vessel_id, structure_id, water_depth_m, hs_m)`` (run_id first).
"""
