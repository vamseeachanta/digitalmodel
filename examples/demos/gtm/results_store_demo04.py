def _git_sha() -> str:
    """Current git SHA, or "unknown" on any failure (detached/missing/non-repo)."""
    try:
        out = subprocess.run(
            ["git", "rev-parse", "HEAD"],
            capture_output=True, text=True, check=True,
            cwd=Path(__file__).resolve().parent,
        )
        return out.stdout.strip() or "unknown"
    except Exception:
        return "unknown"


def _utc_now() -> str:
    return datetime.now(timezone.utc).isoformat()


def _json_safe(value: Any) -> Any:
    """Recursively coerce a config value to a JSON-safe primitive (N4).

    Paths -> str; enums -> .value/.name/str; lists/tuples -> list; dicts -> dict;
    primitives pass through; anything else -> str.
    """
    if value is None or isinstance(value, (bool, int, float, str)):
        return value
    if isinstance(value, Path):
        return str(value)
    if isinstance(value, (list, tuple)):
        return [_json_safe(v) for v in value]
    if isinstance(value, dict):
        return {str(k): _json_safe(v) for k, v in value.items()}
    if hasattr(value, "value"):
        return _json_safe(getattr(value, "value"))
    if hasattr(value, "name"):
        return _json_safe(getattr(value, "name"))
    return str(value)


# Path fields resolve to ABSOLUTE at config-load time (abs-rel-to-yaml-dir). For a
# COMMITTED, portable manifest they must be repo-relative — a client (or another
# machine) must never see this machine's worktree path. Anchor at "examples/" since
# every demo path lives under examples/demos/gtm/.
_PATH_FIELDS = frozenset({"vessels_path", "pipelines_path", "results_root", "output_root", "source_path"})


def _repo_relative(value: Any) -> Any:
    """Return a path string re-anchored at the repo-relative 'examples/...' segment.

    Idempotent for already-relative paths; non-path values pass through unchanged.
    """
    if not isinstance(value, str) or not value:
        return value
    norm = value.replace("\\", "/")
    marker = "examples/"
    idx = norm.rfind(marker)
    return norm[idx:] if idx != -1 else norm


def _serialize_resolved_config(config: Any) -> Dict[str, Any]:
    """Serialize a ResolvedDemo04Config to a JSON-safe dict snapshot (N4).

    Works structurally (no import of the dataclass type) so the module stays
    decoupled. Every field is read via ``getattr`` and made JSON-safe. ``pipe_sizes``
    are tuples; ``_json_safe`` turns them into lists. Path fields are stored
    repo-relative (no machine-specific absolute paths in the committed manifest).
    """
    if config is None:
        return {}
    fields = [
        "demo_id", "code_ref",
        "vessels", "pipe_sizes", "water_depths",
        "grade", "smys_pa", "smts_pa",
        "seawater_density_kg_m3", "gravity_m_s2", "steel_density_kg_m3",
        "youngs_modulus_pa",
        "stress_limit_factor", "tension_margin", "go_threshold",
        "nogo_utilisation", "sagbend_stress_basis",
        "util_color_green_below", "util_color_amber_below",
        "vessels_path", "pipelines_path", "results_root", "output_root",
        "source_path",
    ]
    snapshot: Dict[str, Any] = {}
    for name in fields:
        raw = _json_safe(getattr(config, name, None))
        snapshot[name] = _repo_relative(raw) if name in _PATH_FIELDS else raw
    return snapshot


def _input_ref(config: Any) -> str:
    return _repo_relative(str(getattr(config, "source_path", "") or ""))


def _count_status(records: List[Dict[str, Any]]) -> Dict[str, int]:
    """n_go/n_marginal/n_nogo/n_error from overall_status (GO/MARGINAL/NO_GO/ERROR)."""
    counts = {"n_go": 0, "n_marginal": 0, "n_nogo": 0, "n_error": 0}
    for r in records:
        status = r.get("overall_status")
        if status == "GO":
            counts["n_go"] += 1
        elif status == "MARGINAL":
            counts["n_marginal"] += 1
        elif status == "NO_GO":
            counts["n_nogo"] += 1
        elif status == "ERROR":
            counts["n_error"] += 1
    return counts


