#!/usr/bin/env python3
# ABOUTME: demo_04 Results Store — SQLite fast-read cache + per-run CSV/manifest text source.
# ABOUTME: write_run() persists one run; rebuild_db() regenerates the cache per-cell-identically from text.
"""demo_04 Results Store (sqlite3 stdlib only), adapted from the demo_03 Store.

This module is *additive*: it never changes demo_04's existing JSON or HTML
outputs. Alongside them it adds a queryable artifact set under ``<base_dir>``::

    parametric/demo_04/index.csv                 (one row per run — DERIVED)
    parametric/demo_04/results.db                (SQLite cache — DERIVED, gitignored)
    parametric/demo_04/<run_id>/cases.csv        (enriched per-case table — SOURCE)
    parametric/demo_04/<run_id>/manifest.json    (resolved config snapshot — SOURCE)

The per-run ``cases.csv`` + ``manifest.json`` are the text source of truth;
``index.csv`` and ``results.db`` are derived and regenerated by
:func:`rebuild_db`. The rebuild is verified (tests) to be per-cell and
per-``typeof`` identical to the direct-write path.

demo_04 specifics vs demo_03:

  * BD-1  every check block is built via
          ``record.get("checks", {}).get(<check name>, {}).get(<key>)`` so the
          ERROR path's ``checks={}`` (0 of 60 on the baseline, but the schema must
          survive it) yields NULL with ``typeof='null'`` for every check column,
          on BOTH the direct-write and rebuild paths.
  * BD-2  the five check dicts are flattened into prefixed columns
          (``departure_angle_``/``top_tension_``/``sagbend_``/``overbend_``/
          ``vessel_capability_``) so repeated keys (``status``/``utilisation``)
          never collide. ``status`` and ``stress_basis`` are TEXT tokens; the
          ``*_ok`` / ``in_range`` flags are INTEGER (0/1).
  * BD-3  ``water_depth_m`` is INTEGER. Empty-string / None map to SQL NULL
          *before* any numeric cast, so a null round-trips as NULL.
  * SENT  demo_04 uses ``999.0`` for infeasible utilisations and ``None`` for the
          ERROR-path ``max_utilisation`` / sub-check utils. The store round-trips
          BOTH identically on direct write AND on rebuild-from-cases.csv:
          ``999.0`` stays REAL ``999.0``, ``None`` stays SQL NULL (never ``0`` or
          ``"nan"``).
  * PK    the composite primary key is
          ``(run_id, vessel_id, pipe_size, water_depth_m)`` (run_id first).
"""
from __future__ import annotations

import csv
import json
import re
import sqlite3
import subprocess
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Optional

try:
    import pandas as pd
except ImportError as exc:  # pragma: no cover — real dep required.
    raise RuntimeError("results_store_demo04 requires pandas") from exc


DEMO_ID = "demo_04"

# A run_id becomes a directory name under the Store, so it must be a single safe
# path segment (D1 defense-in-depth: the caller may also validate).
_RUN_ID_RE = re.compile(r"^[A-Za-z0-9._-]+$")


def _validate_run_id(run_id: str) -> str:
    """Reject a run_id that is not a single safe path segment (D1, path-traversal guard)."""
    if not isinstance(run_id, str) or run_id in ("", ".", "..") or not _RUN_ID_RE.match(run_id):
        raise ValueError(
            f"invalid run_id {run_id!r}: must match [A-Za-z0-9._-]+ and may not be "
            "'', '.', or '..' (it becomes a directory name in the Results Store)."
        )
    return run_id


# ---------------------------------------------------------------------------
# Check schema (BD-2): each check contributes prefixed columns. The prefix is the
# check key into record["checks"]; the suffix is the key inside the check dict.
# (prefix, [(suffix, sqlite_type), ...]) — order is canonical.
#
# Type notes:
#   * status / stress_basis are TEXT tokens (never INTEGER).
#   * in_range / diameter_ok / depth_ok are BOOLEAN flags stored as INTEGER (0/1).
#   * utilisation columns are REAL and round-trip the 999.0 infeasible sentinel.
# ---------------------------------------------------------------------------
_CHECK_SPECS: List[tuple[str, List[tuple[str, str]]]] = [
    ("departure_angle", [
        ("required_angle_deg", "REAL"),
        ("stinger_min_deg", "REAL"),
        ("stinger_max_deg", "REAL"),
        ("in_range", "INTEGER"),       # BOOLEAN -> 0/1
        ("status", "TEXT"),
    ]),
    ("top_tension", [
        ("T_top_kn", "REAL"),
        ("T_top_te", "REAL"),
        ("tensioner_capacity_te", "REAL"),
        ("utilisation", "REAL"),       # SENT: 999.0 infeasible sentinel round-trips
        ("status", "TEXT"),
    ]),
    ("sagbend", [
        ("R_sag_m", "REAL"),
        ("kappa_sag", "REAL"),
        ("sigma_bending_mpa", "REAL"),
        ("sigma_axial_mpa", "REAL"),
        ("sigma_combined_mpa", "REAL"),
        ("stress_basis", "TEXT"),
        ("utilisation", "REAL"),       # SENT
        ("status", "TEXT"),
    ]),
    ("overbend", [
        ("R_overbend_m", "REAL"),
        ("sigma_overbend_mpa", "REAL"),
        ("utilisation", "REAL"),       # SENT
        ("status", "TEXT"),
    ]),
    ("vessel_capability", [
        ("pipe_od_in", "REAL"),
        ("max_vessel_diameter_in", "REAL"),
        ("diameter_ok", "INTEGER"),    # BOOLEAN -> 0/1
        ("depth_ok", "INTEGER"),       # BOOLEAN -> 0/1
        ("status", "TEXT"),
    ]),
]


def _check_col(prefix: str, suffix: str) -> str:
    """Prefixed column name, e.g. ``sagbend_utilisation`` (BD-2)."""
    return f"{prefix}_{suffix}"


def _q(identifier: str) -> str:
    """Quote a SQL identifier so any non-bare-word column name is legal in DDL/DML
    (SQLite double-quote identifier escaping). Mirrors the demo_03 store's hyphen-safe
    quoter so the idiom is consistent even though demo_04 has no hyphenated columns."""
    return '"' + identifier.replace('"', '""') + '"'


# ---------------------------------------------------------------------------
# Explicit typed schema for the `cases` table (every column an explicit type).
# 19 top-level + 27 check columns = 46. All columns are NULLABLE.
# Order is canonical for CSV + SQLite on BOTH paths. `run_id` is first (PK part).
# ---------------------------------------------------------------------------
_TOP_COLUMNS: List[tuple[str, str]] = [
    ("run_id", "TEXT"),
    ("vessel_id", "TEXT"),
    ("vessel_name", "TEXT"),
    ("pipe_size", "TEXT"),
    ("pipe_od_mm", "REAL"),
    ("pipe_wt_mm", "REAL"),
    ("water_depth_m", "INTEGER"),     # BD-3
    ("overall_status", "TEXT"),
    ("max_utilisation", "REAL"),      # SENT: None -> NULL on ERROR path
    ("governing_check", "TEXT"),
    ("H_chosen_kn", "REAL"),
    ("T_top_te", "REAL"),
    ("departure_angle_deg", "REAL"),
    ("sagbend_util", "REAL"),         # SENT: None -> NULL on ERROR path
    ("overbend_util", "REAL"),        # SENT
    ("tension_util", "REAL"),         # SENT
    ("sagbend_stress_mpa", "REAL"),
    ("overbend_stress_mpa", "REAL"),
    ("R_sag_m", "REAL"),
]

_CASE_COLUMNS: List[tuple[str, str]] = list(_TOP_COLUMNS)
for _prefix, _suffixes in _CHECK_SPECS:
    for _suffix, _typ in _suffixes:
        _CASE_COLUMNS.append((_check_col(_prefix, _suffix), _typ))

assert len(_CASE_COLUMNS) == 46, f"expected 46 case columns, got {len(_CASE_COLUMNS)}"

_CASE_COL_NAMES = [name for name, _ in _CASE_COLUMNS]
_CASE_COL_TYPES = {name: typ for name, typ in _CASE_COLUMNS}

# The BOOLEAN-origin INTEGER columns (flattened from Python bools in the checks).
_BOOL_COLUMNS = frozenset({
    "departure_angle_in_range",
    "vessel_capability_diameter_ok",
    "vessel_capability_depth_ok",
})

# composite primary key (run_id first).
_PK_COLUMNS = ("run_id", "vessel_id", "pipe_size", "water_depth_m")

_RUNS_COLUMNS: List[tuple[str, str]] = [
    ("run_id", "TEXT PRIMARY KEY"),
    ("utc", "TEXT"),
    ("resolved_yaml", "TEXT"),
    ("git_sha", "TEXT"),
    ("total_cases", "INTEGER"),
    ("n_go", "INTEGER"),
    ("n_marginal", "INTEGER"),
    ("n_nogo", "INTEGER"),
    ("n_error", "INTEGER"),
]

_INDEX_COLUMNS = [
    "run_id", "utc", "total_cases", "n_go", "n_marginal", "n_nogo", "n_error",
    "input_ref", "git_sha",
]


