def _flatten_record(run_id: str, rec: Dict[str, Any]) -> Dict[str, Any]:
    """Map a demo_04 record into the flat 46-column dict (BD-1 / BD-2).

    Check blocks are read defensively via
    ``rec.get("checks", {}).get(<check>, {}).get(<key>)`` so the ERROR path's
    ``checks={}`` produces None (-> NULL) for every check column. The built row's
    key set is asserted to equal the declared column set, so a schema drift fails
    loudly.
    """
    checks = rec.get("checks") or {}
    row: Dict[str, Any] = {
        "run_id": run_id,
        "vessel_id": rec.get("vessel_id"),
        "vessel_name": rec.get("vessel_name"),
        "pipe_size": rec.get("pipe_size"),
        "pipe_od_mm": rec.get("pipe_od_mm"),
        "pipe_wt_mm": rec.get("pipe_wt_mm"),
        "water_depth_m": rec.get("water_depth_m"),
        "overall_status": rec.get("overall_status"),
        "max_utilisation": rec.get("max_utilisation"),
        "governing_check": rec.get("governing_check"),
        "H_chosen_kn": rec.get("H_chosen_kn"),
        "T_top_te": rec.get("T_top_te"),
        "departure_angle_deg": rec.get("departure_angle_deg"),
        "sagbend_util": rec.get("sagbend_util"),
        "overbend_util": rec.get("overbend_util"),
        "tension_util": rec.get("tension_util"),
        "sagbend_stress_mpa": rec.get("sagbend_stress_mpa"),
        "overbend_stress_mpa": rec.get("overbend_stress_mpa"),
        "R_sag_m": rec.get("R_sag_m"),
    }
    for prefix, suffixes in _CHECK_SPECS:
        check_block = checks.get(prefix, {}) or {}
        for suffix, _typ in suffixes:
            row[_check_col(prefix, suffix)] = check_block.get(suffix)

    declared = set(_CASE_COL_NAMES)
    assert set(row.keys()) == declared, (
        "demo_04 case row keys do not match declared columns: "
        f"{sorted(set(row.keys()) ^ declared)}"
    )
    return row


def _coerce_cell(value: Any, sqlite_type: str, col: str) -> Any:
    """Coerce a value (native or CSV-read string) to the typed cell value.

    BD-3 / SENT / rebuild determinism: empty string and ``None`` both map to SQL
    NULL *before* any numeric cast, for ANY nullable column — so a null round-trips
    as NULL, never ``0`` or the string ``"nan"``. The 999.0 infeasible sentinel is
    just a REAL value and round-trips as REAL 999.0.

    BOOLEAN-origin INTEGER columns (``_BOOL_COLUMNS``) accept native Python bools
    (``True``/``False``) and the CSV string forms (``"True"``/``"False"``/``"1"``/
    ``"0"``), normalising all to 0/1 so a bool round-trips identically through CSV.
    """
    if value is None:
        return None
    if isinstance(value, str) and value == "":
        return None
    if col in _BOOL_COLUMNS:
        # Native bool, numeric, or the CSV string forms — all -> 0/1.
        if isinstance(value, bool):
            return int(value)
        if isinstance(value, (int, float)):
            return int(value)
        s = str(value).strip()
        if s in ("True", "true", "1", "1.0"):
            return 1
        if s in ("False", "false", "0", "0.0"):
            return 0
        return int(float(s))
    if sqlite_type == "REAL":
        return float(value)
    if sqlite_type == "INTEGER":
        # bool is an int subclass — int(True) == 1 (correct), but it is only reached
        # here for non-bool INTEGER columns (e.g. water_depth_m).
        return int(value)
    return str(value)


def _csv_cell(value: Any, col: str) -> str:
    """Render a flattened cell for CSV. None -> ""; BOOLEAN-origin cols -> "0"/"1"
    (so a Python ``True`` does NOT serialise as the string ``"True"`` — keeping the
    REAL/INTEGER round-trip clean and the rebuild per-cell identical)."""
    if value is None:
        return ""
    if col in _BOOL_COLUMNS:
        if isinstance(value, bool):
            return "1" if value else "0"
        return "1" if int(value) else "0"
    return str(value)


def _typed_case_row(run_id: str, raw: Dict[str, Any], *, flatten: bool) -> tuple:
    """Build a typed value tuple in canonical column order.

    When *flatten* is True, *raw* is a demo_04 record (nested checks); otherwise
    *raw* is a flat CSV row dict (strings). Both produce per-cell identical typed
    tuples.
    """
    flat = _flatten_record(run_id, raw) if flatten else raw
    values: List[Any] = []
    for name in _CASE_COL_NAMES:
        if name == "run_id":
            values.append(run_id)
            continue
        values.append(_coerce_cell(flat.get(name), _CASE_COL_TYPES[name], name))
    return tuple(values)


# ---------------------------------------------------------------------------
# SQLite schema + transactional per-run write
# ---------------------------------------------------------------------------

def _create_schema(conn: sqlite3.Connection) -> None:
    runs_cols = ", ".join(f"{_q(n)} {t}" for n, t in _RUNS_COLUMNS)
    case_cols = ", ".join(f"{_q(n)} {t}" for n, t in _CASE_COLUMNS)
    pk = ", ".join(_q(n) for n in _PK_COLUMNS)
    conn.execute(f"CREATE TABLE IF NOT EXISTS runs ({runs_cols})")
    conn.execute(f"CREATE TABLE IF NOT EXISTS cases ({case_cols}, PRIMARY KEY ({pk}))")


def _upsert_run_into_db(
    conn: sqlite3.Connection,
    run_id: str,
    run_row: Dict[str, Any],
    case_rows: List[tuple],
) -> None:
    """Transactionally replace a run: delete-then-insert (never duplicate/orphan)."""
    placeholders = ", ".join("?" for _ in _CASE_COL_NAMES)
    run_cols = [n for n, _ in _RUNS_COLUMNS]
    run_placeholders = ", ".join("?" for _ in run_cols)
    conn.execute("BEGIN")
    try:
        conn.execute("DELETE FROM cases WHERE run_id=?", (run_id,))
        conn.execute("DELETE FROM runs WHERE run_id=?", (run_id,))
        conn.execute(
            f"INSERT INTO runs ({', '.join(_q(c) for c in run_cols)}) "
            f"VALUES ({run_placeholders})",
            tuple(run_row[c] for c in run_cols),
        )
        conn.executemany(
            f"INSERT INTO cases ({', '.join(_q(c) for c in _CASE_COL_NAMES)}) "
            f"VALUES ({placeholders})",
            case_rows,
        )
        conn.execute("COMMIT")
    except Exception:
        conn.execute("ROLLBACK")
        raise


