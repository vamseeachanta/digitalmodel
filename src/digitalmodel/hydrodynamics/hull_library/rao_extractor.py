class NativeXlsxReader:
    """Read RAO data from native OrcaWave xlsx export files.

    Native format has sheets with 2-3 header rows, then structured tables.
    Key sheets:
      - 'Displacement RAOs': per-heading blocks of (freq, surge_amp, surge_phase, ...)
      - 'Added mass': per-frequency 6x6 matrix blocks
      - 'Damping': per-frequency 6x6 matrix blocks
    """

    def __init__(self, xlsx_path: Path) -> None:
        self._path = Path(xlsx_path)
        self._wb = openpyxl.load_workbook(self._path, data_only=True)

    def read_rao_data(self, vessel_name: str | None = None) -> RAOData:
        """Extract displacement RAOs from native OrcaWave format.

        The 'Displacement RAOs' sheet layout:
          - Rows 1-9: headers (sheet title, version, DOF labels, unit labels)
          - Data rows: [heading_deg, frequency_rad_s, surge_amp, surge_phase,
            sway_amp, sway_phase, heave_amp, heave_phase, roll_amp, roll_phase,
            pitch_amp, pitch_phase, yaw_amp, yaw_phase]
          - The heading value in column 0 groups data into heading blocks.
        """
        ws = self._wb["Displacement RAOs"]
        all_rows = list(ws.iter_rows(values_only=True))

        # Parse data rows — skip header/label rows
        # Data rows have numeric values in columns 0 and 1
        data_by_heading: dict[float, list[tuple[float, list[float]]]] = {}

        for row in all_rows:
            if row[0] is None or row[1] is None:
                continue
            try:
                heading = float(row[0])
                freq_rad_s = float(row[1])
            except (ValueError, TypeError):
                continue

            # Extract 12 data values (6 DOFs × 2: amp + phase)
            vals = []
            for v in row[2:14]:
                try:
                    vals.append(float(v) if v is not None else 0.0)
                except (ValueError, TypeError):
                    vals.append(0.0)

            if len(vals) >= 12:
                if heading not in data_by_heading:
                    data_by_heading[heading] = []
                data_by_heading[heading].append((freq_rad_s, vals))

        if not data_by_heading:
            raise ValueError(
                f"No displacement RAO data found in {self._path}"
            )

        # Sort headings to get consistent ordering
        sorted_headings = sorted(data_by_heading.keys())
        directions = np.array(sorted_headings, dtype=np.float64)

        # Get frequencies from the first heading block
        first_block = data_by_heading[sorted_headings[0]]
        frequencies = np.array([f for f, _ in first_block], dtype=np.float64)
        n_freq = len(frequencies)
        n_dir = len(directions)

        # Build arrays
        amplitudes = np.zeros((n_freq, n_dir, 6), dtype=np.float64)
        phases = np.zeros((n_freq, n_dir, 6), dtype=np.float64)

        for dir_idx, heading in enumerate(sorted_headings):
            block = data_by_heading[heading]
            for freq_idx, (_, vals) in enumerate(block):
                for dof_idx in range(6):
                    amp_col = dof_idx * 2       # 0, 2, 4, 6, 8, 10
                    phase_col = dof_idx * 2 + 1  # 1, 3, 5, 7, 9, 11
                    amplitudes[freq_idx, dir_idx, dof_idx] = abs(vals[amp_col])
                    phases[freq_idx, dir_idx, dof_idx] = vals[phase_col]

        # Sort by frequency ascending
        sort_idx = np.argsort(frequencies)
        frequencies = frequencies[sort_idx]
        amplitudes = amplitudes[sort_idx, :, :]
        phases = phases[sort_idx, :, :]

        name = vessel_name or self._path.stem

        return RAOData(
            frequencies=frequencies,
            directions=directions,
            amplitudes=amplitudes,
            phases=phases,
            vessel_name=name,
        )

    def read_hydro_coefficients(self) -> HydroCoefficients:
        """Extract added mass and damping from native format.

        Native format has these sheets with per-frequency 6x6 blocks.
        """
        am = self._read_native_matrix_sheet("Added mass")
        damp = self._read_native_matrix_sheet("Damping")
        return HydroCoefficients(
            frequencies=am[0],
            added_mass=am[1],
            damping=damp[1],
        )

    def _read_native_matrix_sheet(
        self, sheet_name: str
    ) -> tuple[np.ndarray, np.ndarray]:
        """Parse a native-format 6x6 matrix sheet.

        Each frequency block is structured as:
          [blank row]
          [None, '{sheet_name} for frequency X.Xrad/s', ...]
          [None, 'DOF', ...]
          [None, body_name, ...]
          [None, 'Surge', 'Sway', 'Heave', 'Roll', 'Pitch', 'Yaw']
          [None, '1', '2', '3', '4', '5', '6']
          ['1', val, val, val, val, val, val]
          ['2', val, val, val, val, val, val]
          ... 6 data rows
        """
        ws = self._wb[sheet_name]
        all_rows = list(ws.iter_rows(values_only=True))

        freq_pattern = re.compile(
            r"frequency\s+([\d.eE+-]+)\s*rad",
            re.IGNORECASE,
        )

        frequencies: list[float] = []
        matrices: list[np.ndarray] = []

        i = 0
        while i < len(all_rows):
            row = all_rows[i]
            # Look for frequency marker in column 1
            if row[1] is not None and isinstance(row[1], str):
                m = freq_pattern.search(row[1])
                if m:
                    freq_rad_s = float(m.group(1))

                    # Skip to the data rows (after DOF label, body name,
                    # Surge/Sway header, and 1/2/3 numbering rows)
                    i += 1
                    # Find the numbered column header row (1, 2, 3, 4, 5, 6)
                    while i < len(all_rows):
                        check = all_rows[i]
                        if (check[1] is not None
                                and str(check[1]).strip() == "1"
                                and check[2] is not None
                                and str(check[2]).strip() == "2"):
                            i += 1  # skip to first data row
                            break
                        i += 1

                    # Read 6 data rows — each starts with row number in col 0
                    matrix = np.zeros((6, 6), dtype=np.float64)
                    for row_idx in range(6):
                        if i + row_idx >= len(all_rows):
                            break
                        data_row = all_rows[i + row_idx]
                        for col_idx in range(6):
                            val = data_row[col_idx + 1] if col_idx + 1 < len(data_row) else 0.0
                            try:
                                matrix[row_idx, col_idx] = float(val) if val is not None else 0.0
                            except (ValueError, TypeError):
                                matrix[row_idx, col_idx] = 0.0

                    frequencies.append(freq_rad_s)
                    matrices.append(matrix)
                    i += 6
                    continue
            i += 1

        if not frequencies:
            raise ValueError(
                f"No matrix data found in sheet '{sheet_name}' of {self._path}"
            )

        freq_arr = np.array(frequencies, dtype=np.float64)
        mat_arr = np.stack(matrices)

        # Sort ascending
        sort_idx = np.argsort(freq_arr)
        freq_arr = freq_arr[sort_idx]
        mat_arr = mat_arr[sort_idx, :, :]

        return freq_arr, mat_arr


