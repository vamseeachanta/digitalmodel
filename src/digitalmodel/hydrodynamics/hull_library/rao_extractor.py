"""
ABOUTME: Extract RAO data and hydrodynamic coefficients from OrcaWave .xlsx
sidecar files and populate the RAODatabase.

Supports two xlsx formats:
  1. "Pipeline" format — generated by process-queue.py on licensed-win-1.
     Sheets: Summary, RAOs, AddedMass, Damping, Discretization.
  2. "Native" format — exported by OrcaWave GUI on Windows.
     Sheets: Displacement RAOs, Added mass, Damping, etc.

This module does NOT require OrcFxAPI — it reads only xlsx files,
making it usable on dev-primary without a solver license.

Traceability: #1765, #1597
"""
from __future__ import annotations

import re
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any

import numpy as np
import openpyxl

from digitalmodel.hydrodynamics.models import RAOData
from digitalmodel.hydrodynamics.hull_library.rao_database import (
    RAODatabase,
    RAODatabaseEntry,
)

# DOF ordering used throughout — matches OrcaWave convention
DOF_NAMES = ["Surge", "Sway", "Heave", "Roll", "Pitch", "Yaw"]


# ---------------------------------------------------------------------------
# Data models
# ---------------------------------------------------------------------------


@dataclass
class HydroCoefficients:
    """Added mass and damping coefficient matrices.

    Attributes
    ----------
    frequencies : np.ndarray
        Frequencies in rad/s, shape (n_freq,), ascending.
    added_mass : np.ndarray
        Added mass matrices, shape (n_freq, 6, 6).
    damping : np.ndarray
        Damping matrices, shape (n_freq, 6, 6).
    """

    frequencies: np.ndarray
    added_mass: np.ndarray
    damping: np.ndarray


# ---------------------------------------------------------------------------
# Format detector
# ---------------------------------------------------------------------------


class XlsxFormatDetector:
    """Detect whether an xlsx file uses pipeline or native OrcaWave format."""

    @staticmethod
    def detect(xlsx_path: Path) -> str:
        """Return 'pipeline' or 'native'.

        Pipeline format has sheets: Summary, RAOs, AddedMass, Damping, Discretization.
        Native format has sheets like: Displacement RAOs, Added mass, Damping, etc.

        Raises
        ------
        FileNotFoundError
            If the file does not exist.
        ValueError
            If the format cannot be determined.
        """
        xlsx_path = Path(xlsx_path)
        if not xlsx_path.exists():
            raise FileNotFoundError(f"File not found: {xlsx_path}")

        wb = openpyxl.load_workbook(xlsx_path, read_only=True, data_only=True)
        sheet_names = set(wb.sheetnames)
        wb.close()

        # Pipeline format markers
        if "RAOs" in sheet_names and "AddedMass" in sheet_names:
            return "pipeline"

        # Native format markers
        if "Displacement RAOs" in sheet_names or "Added mass" in sheet_names:
            return "native"

        raise ValueError(
            f"Cannot determine xlsx format. Sheets: {sorted(sheet_names)}"
        )


# ---------------------------------------------------------------------------
# Pipeline format reader
# ---------------------------------------------------------------------------


class PipelineXlsxReader:
    """Read RAO data from pipeline-format xlsx files.

    Pipeline format (generated by process-queue.py):
      - Summary sheet: job metadata
      - RAOs sheet: columns = [Frequency (rad/s), Period (s),
        {DOF}_Mag_H{heading}, {DOF}_Phase_H{heading}, ...]
      - AddedMass sheet: columns = [Frequency (rad/s), Period (s),
        {DOFi}_{DOFj}, ...]
      - Damping sheet: same layout as AddedMass
      - Discretization sheet: frequency/period table
    """

    def __init__(self, xlsx_path: Path) -> None:
        self._path = Path(xlsx_path)
        self._wb = openpyxl.load_workbook(self._path, data_only=True)

    def read_rao_data(self, vessel_name: str | None = None) -> RAOData:
        """Extract 6-DOF RAO amplitude + phase arrays.

        Returns
        -------
        RAOData
            With frequencies in rad/s (ascending), directions in degrees,
            amplitudes shape (n_freq, n_dir, 6), phases shape (n_freq, n_dir, 6).
        """
        ws = self._wb["RAOs"]
        headers = [cell.value for cell in ws[1]]
        data_rows = list(ws.iter_rows(min_row=2, values_only=True))

        # Extract frequencies (already in rad/s, ascending from row order)
        frequencies = np.array([row[0] for row in data_rows], dtype=np.float64)

        # Parse headings from column names: {DOF}_Mag_H{heading}
        heading_set: list[float] = []
        heading_pattern = re.compile(r"_Mag_H([\d.]+)$")
        seen_headings: set[float] = set()
        for h in headers:
            if h is None:
                continue
            m = heading_pattern.search(h)
            if m:
                hdg = float(m.group(1))
                if hdg not in seen_headings:
                    seen_headings.add(hdg)
                    heading_set.append(hdg)

        directions = np.array(heading_set, dtype=np.float64)
        n_freq = len(frequencies)
        n_dir = len(directions)

        # Build column index map: (dof_idx, heading_idx) → (mag_col, phase_col)
        col_map: dict[tuple[int, int], tuple[int, int]] = {}
        for dof_idx, dof_name in enumerate(DOF_NAMES):
            for dir_idx, hdg in enumerate(directions):
                mag_col_name = f"{dof_name}_Mag_H{hdg}"
                phase_col_name = f"{dof_name}_Phase_H{hdg}"
                mag_col = headers.index(mag_col_name)
                phase_col = headers.index(phase_col_name)
                col_map[(dof_idx, dir_idx)] = (mag_col, phase_col)

        # Fill arrays
        amplitudes = np.zeros((n_freq, n_dir, 6), dtype=np.float64)
        phases = np.zeros((n_freq, n_dir, 6), dtype=np.float64)

        for freq_idx, row in enumerate(data_rows):
            for (dof_idx, dir_idx), (mag_col, phase_col) in col_map.items():
                amplitudes[freq_idx, dir_idx, dof_idx] = abs(float(row[mag_col] or 0.0))
                phases[freq_idx, dir_idx, dof_idx] = float(row[phase_col] or 0.0)

        # Ensure frequencies are ascending
        if len(frequencies) > 1 and frequencies[0] > frequencies[-1]:
            sort_idx = np.argsort(frequencies)
            frequencies = frequencies[sort_idx]
            amplitudes = amplitudes[sort_idx, :, :]
            phases = phases[sort_idx, :, :]

        name = vessel_name or self._path.stem
        return RAOData(
            frequencies=frequencies,
            directions=directions,
            amplitudes=amplitudes,
            phases=phases,
            vessel_name=name,
        )

    def read_hydro_coefficients(self) -> HydroCoefficients:
        """Extract 6x6 added mass and damping matrices per frequency.

        Returns
        -------
        HydroCoefficients
            With frequencies ascending, matrices shape (n_freq, 6, 6).
        """
        am_data = self._read_matrix_sheet("AddedMass")
        damp_data = self._read_matrix_sheet("Damping")
        return HydroCoefficients(
            frequencies=am_data[0],
            added_mass=am_data[1],
            damping=damp_data[1],
        )

    def _read_matrix_sheet(
        self, sheet_name: str
    ) -> tuple[np.ndarray, np.ndarray]:
        """Read a 6x6 matrix sheet (AddedMass or Damping).

        Returns (frequencies, matrices) where matrices has shape (n_freq, 6, 6).
        """
        ws = self._wb[sheet_name]
        headers = [cell.value for cell in ws[1]]
        data_rows = list(ws.iter_rows(min_row=2, values_only=True))

        frequencies = np.array([row[0] for row in data_rows], dtype=np.float64)
        n_freq = len(frequencies)

        # Build column index map: (i, j) → col_idx
        col_map: dict[tuple[int, int], int] = {}
        for i, dof_i in enumerate(DOF_NAMES):
            for j, dof_j in enumerate(DOF_NAMES):
                col_name = f"{dof_i}_{dof_j}"
                col_map[(i, j)] = headers.index(col_name)

        matrices = np.zeros((n_freq, 6, 6), dtype=np.float64)
        for freq_idx, row in enumerate(data_rows):
            for (i, j), col_idx in col_map.items():
                matrices[freq_idx, i, j] = float(row[col_idx] or 0.0)

        # Ensure ascending
        if n_freq > 1 and frequencies[0] > frequencies[-1]:
            sort_idx = np.argsort(frequencies)
            frequencies = frequencies[sort_idx]
            matrices = matrices[sort_idx, :, :]

        return frequencies, matrices


