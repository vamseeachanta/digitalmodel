def xlsx_to_rao_data(
    xlsx_path: Path, vessel_name: str | None = None
) -> RAOData:
    """Auto-detect format and extract RAO data from an OrcaWave xlsx file.

    Parameters
    ----------
    xlsx_path : Path
        Path to .xlsx file (pipeline or native format).
    vessel_name : str, optional
        Override the vessel name. Defaults to filename stem.

    Returns
    -------
    RAOData
        With frequencies in rad/s, directions in degrees,
        amplitudes and phases shape (n_freq, n_dir, 6).
    """
    xlsx_path = Path(xlsx_path)
    fmt = XlsxFormatDetector.detect(xlsx_path)
    if fmt == "pipeline":
        reader = PipelineXlsxReader(xlsx_path)
    else:
        reader = NativeXlsxReader(xlsx_path)
    return reader.read_rao_data(vessel_name=vessel_name)


def xlsx_to_hydro_coefficients(xlsx_path: Path) -> HydroCoefficients:
    """Auto-detect format and extract hydrodynamic coefficients.

    Parameters
    ----------
    xlsx_path : Path
        Path to .xlsx file (pipeline or native format).

    Returns
    -------
    HydroCoefficients
        Added mass and damping matrices, shape (n_freq, 6, 6).
    """
    xlsx_path = Path(xlsx_path)
    fmt = XlsxFormatDetector.detect(xlsx_path)
    if fmt == "pipeline":
        reader = PipelineXlsxReader(xlsx_path)
    else:
        reader = NativeXlsxReader(xlsx_path)
    return reader.read_hydro_coefficients()


def populate_database_from_xlsx(
    db: RAODatabase,
    xlsx_path: Path,
    variation_id: str,
    hull_params: dict[str, float],
    metadata: dict[str, Any] | None = None,
    vessel_name: str | None = None,
) -> RAODatabaseEntry:
    """Extract RAO data from xlsx and store in the RAODatabase.

    Parameters
    ----------
    db : RAODatabase
        Target database.
    xlsx_path : Path
        Path to .xlsx file.
    variation_id : str
        Unique ID for this hull variation.
    hull_params : dict
        Hull parametric scaling values.
    metadata : dict, optional
        Extra metadata (solver version, case name, etc.).
    vessel_name : str, optional
        Override vessel name.

    Returns
    -------
    RAODatabaseEntry
        The entry that was stored.
    """
    xlsx_path = Path(xlsx_path)
    rao_data = xlsx_to_rao_data(xlsx_path, vessel_name=vessel_name)

    entry_metadata = metadata.copy() if metadata else {}
    entry_metadata.setdefault("source_file", str(xlsx_path))

    entry = RAODatabaseEntry(
        variation_id=variation_id,
        hull_params=hull_params,
        rao_data=rao_data,
        metadata=entry_metadata,
    )
    db.store(entry)
    return entry


